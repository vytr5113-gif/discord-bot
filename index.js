client.on("messageCreate", async (message) => {
  if (message.author.bot) return;

  const text = message.content.trim().toLowerCase();

  // Антидебил-фильтр
  const ignoredMessages = [
    "а",
    "ок",
    "окей",
    "лол",
    "кек",
    "?",
    "??",
    ".",
    "!",
    "спс",
    "спасибо",
    "пон",
    "ясно",
    "угу",
    "ага",
    "хм",
    "мм",
  ];

  if (ignoredMessages.includes(text)) return;
  if (text.length < 3) return;

  // Отвечать только в нужных каналах
  const allowedChannels = [
    "поддержка",
    "ticket",
    "тикет",
  ];

  const channelName = message.channel.name?.toLowerCase() || "";

  const isAllowedChannel = allowedChannels.some((name) =>
    channelName.includes(name)
  );

  if (!isAllowedChannel) return;

  // Жёсткий ответ на вайп
  if (
    text.includes("когда вайп") ||
    text.includes("следующий вайп") ||
    text.includes("во сколько вайп")
  ) {
    return message.reply("Вайп проходит каждый понедельник в 17:00 🙂");
  }

  try {
    const response = await openai.chat.completions.create({
      model: process.env.OPENAI_MODEL || "gpt-4o-mini",
      messages: [
        { role: "system", content: SYSTEM_PROMPT },
        { role: "user", content: message.content },
      ],
    });

    const reply = response.choices[0].message.content?.trim();

    if (!reply) return;

    await message.reply(reply.slice(0, 1900));
  } catch (error) {
    console.error(error);

    await message.reply(
      "Понял, это технический момент, подключаю администрацию 🙂\n@administrator @moderator"
    );
  }
});

